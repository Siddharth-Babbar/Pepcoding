public class evenOddNum
{
  public static void main(String[] args)
  {
    System.out.print("Even number :");
    for(int i=1;i<=50;i++)
     {
       System.out.print(" " + 2*i);
     }
    System.out.println();
    System.out.print("Odd number :");
    for(int i=0;i<=49;i++)
     {
       System.out.print(" " + (2*i +1));
     }
  }

}